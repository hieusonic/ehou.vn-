const getAllPaths = (menus: TMenus) => {
  const paths: string[] = [];
  menus.forEach((menu) => {
    if (menu.path !== "#") paths.push(menu.path);
    if (menu?.childs) {
      paths.push(...getAllPaths(menu?.childs));
    }
  });
  return paths;
};

